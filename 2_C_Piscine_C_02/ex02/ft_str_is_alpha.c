int	ft_str_is_alpha(char *str)
{
	int	i;
	int	k;

	i = 0;
	k = 1;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (*(str))
	{
		if ((*str >= 'a' && *str <= 'z')
			|| (*str >= 'A' && *str <= 'Z'))
		{
			k *= 1;
		}
		else
		{
			k *= 0;
		}
		str++;
	}
	return (k);
}
