#include <unistd.h>
void	ft_rev_int_tab (int *tab, int size)
{	
	int		i;
	char	tab2;

	i = -1;
	while (--size > ++i)
	{
		tab2 = tab [i];
		tab[i] = tab[size];
		tab[size] = tab2;
	}
}
