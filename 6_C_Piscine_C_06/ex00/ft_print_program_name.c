#include<unistd.h>

int	ft_strlen(char *str)
{
	int	str_len;

	str_len = 0;
	while (str[str_len] != '\0')
	{
		str_len++;
	}
	return (str_len);
}

int	main(int argc, char *argv[])
{
	write(1, argv[0], ft_strlen(argv[0]));
	write(1, "\n", argc);
}
